๏ปฟ; ๏ฟฝ๏ฟฝๆฅค๏ฟฝ๏ฟฝ ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ แจฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ็ญฎ๏ฟฝ ๏ฟฝแซฎ.
; ๏ฟฝๅฎค - ds:bx   - ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ
; ๏ฟฝ๏ฟฝๅฎค - ax - ๏ฟฝแซฎ ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
;
str2bin  proc
	push bx
	push cx
	sub ax,ax        ; ๏ฟฝ๏ฟฝ๏ฟฝใซจ๏ฟฝ๏ฟฝ ax
	sub ch,ch        ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ch
        mov cl,byte ptr [bx]  ; ๏ฟฝ๏ฟฝ๏ฟฝ็จช แจฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ - ๏ฟฝ CX
blanks: cmp byte ptr[bx+1],' '   ;ๆจช๏ฟฝ ๏ฟฝเฎฏ๏ฟฝแช  ๏ฟฝเฎก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        jne chk_neg
	inc bx
        loop blanks
        jmp  good        ; ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝใฆฅ๏ฟฝ๏ฟฝ ๏ฟฝแฅซ. เฅง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ - 0
;
; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ้จฅ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๋ฏฎ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝ็ ฅ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ  "-"
;
chk_neg: cmp byte ptr [bx+1],'-'
	jne chk_pos
	inc bx               ; ๏ฟฝเฎค๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ใช ๏ฟฝ๏ฟฝโฅซ๏ฟฝ
	dec cx               ; ใฌฅ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ็จช
	call conv_ab         ; ๏ฟฝ๋งข๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝเฎฃเ ฌ๏ฟฝ๏ฟฝ ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        jc thru              ;
	cmp ax,32768         ; ๏ฟฝแซฎ < -32768
	ja  no_good          ; ๏ฟฝ๏ฟฝ, ๏ฟฝ่จก๏ฟฝ๏ฟฝ
        neg ax               ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝแซ 
        js  good             ; o'key , ๏ฟฝ๏ฟฝๅฎค๏ฟฝ๏ฟฝ
;
; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ้จฅ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๋ฏฎ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝ็ ฅ, ๏ฟฝแซจ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
;
chk_pos: cmp byte ptr [bx+1],'+'   ; sign '+' ?
        jne go_conv           ; ๏ฟฝ๏ฟฝ๏ฟฝ , ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        inc bx                ; แฌฅ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ใช ๏ฟฝ๏ฟฝโฅซ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ
        dec cx                ; ใฌฅ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ็จช แจฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
go_conv: call conv_ab         ; ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ ๏ฟฝ ๏ฟฝแซฎ
        jc thru               ; ๏ฟฝ ๏ฟฝ๏ฟฝ็ ฅ ๏ฟฝ่จก๏ฟฝ๏ฟฝ - ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝๅฎค
        cmp ax,32767          ; ๏ฟฝแซฎ ๏ฟฝ๏ฟฝ แซจ่ชฎ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ?
        ja no_good            ; แซจ่ชฎ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ, ๏ฟฝ่จก๏ฟฝ๏ฟฝ
good:   clc                   ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ไซ ๏ฟฝ CY
        jnc thru              
no_good: stc                  ; set carry flag
thru:   pop cx                ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝโ ญ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ็ฅญ๏ฟฝ๏ฟฝ เฅฃ๏ฟฝ๏ฟฝ๏ฟฝเฎข
	pop bx
	jmp skipit
;
; ๏ฟฝ๏ฟฝๆฅค๏ฟฝ๏ฟฝ แฎก๏ฟฝโขฅ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝแซ 
;
conv_ab proc
	push bp
	push bx
	push si
	mov  bp,bx      ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ใช ๏ฟฝ๏ฟฝโฅซ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ ๏ฟฝ bp
        sub  bx,bx      ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ bx
range:  cmp byte ptr ds:[bp+1],'0'  ; แจฌ๏ฟฝ๏ฟฝ๏ฟฝ - ๏ฟฝ๏ฟฝ๏ฟฝ ?
        jb  non_dig                 ; ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ
        cmp byte ptr ds:[bp+1],'9'  ; แจฌ๏ฟฝ๏ฟฝ๏ฟฝ > '9'
        jbe digit                   ; แจฌ๏ฟฝ๏ฟฝ๏ฟฝ - ๏ฟฝ๏ฟฝ๏ฟฝ
non_dig: stc                        ; ๏ฟฝ๏ฟฝโ ญ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ไซ ๏ฟฝ ๏ฟฝ่จก๏ฟฝ๏ฟฝ
        jc end_conv                 ; ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝๆฅค๏ฟฝ๏ฟฝ๏ฟฝ
digit:  mov si,10
	  push dx                     ; ๏ฟฝ๏ฟฝเ ญ๏ฟฝ๏ฟฝ๏ฟฝ dx
        mul si                      ; (ax)*(si)->(dx;ax)
        pop dx                      ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝโ ญ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ dx
        mov bl,ds:[bp+1]            ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฅค๏ฟฝ๏ฟฝ๏ฟฝ แจฌ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ
        and bx,0fh                  ; ๏ฟฝเฅฎ๏ฟฝเ ง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ แจฌ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ็ญฎ๏ฟฝ
        add ax,bx                   ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝเฎฌ๏ฟฝ เฅง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        jc  end_conv                ; ๏ฟฝแซจ เฅง๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ แซจ่ชฎ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ,๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ
        inc bp                      ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ แซฅ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝเฎช๏ฟฝ
        loop range                  ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๆจช๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ แจฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        clc                         ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ไซ ๏ฟฝ ๏ฟฝ๏ฟฝเฅญ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ่จก๏ฟฝ๏ฟฝ)
end_conv:   pop si                    ; ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝโ ญ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ เฅฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ
         pop bx
         pop bp
         ret
conv_ab  endp
skipit:  ret
str2bin  endp
